"""swap in python"""

# create two variables and swap them

X = 0
Y = 1

# print value of x, y before swap
print("before swap:")
print(f"x = {X}")
print(f"y = {Y}")
print("====================")

# swap x and y
X , Y = Y , X


# print value of x, y after swap
print("after swap: ")
print(f"x = {X}")
print(f"y = {Y}")
