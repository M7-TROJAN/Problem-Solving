""" mathematical operators """


# addition
print(5 + 5) # 8

#subtraction
print(5 - 2) # 3

#multiplication
print(5 * 2) # 10

#power
print(5 ** 2) # 25

#division
print(5 / 2) # 2.5

# ZeroDivisionError
print(5 / 0) # Error You Cannot division by zero
print(5 % 0) # Error You Cannot division or modulus by zero

#integer division
print(5 // 2) # 2

# modulus
print(5 % 2) # 1


""" mathematical precedence """
# ()
print((5+1)*2) # 16

# **
print(5**2 * 4) # 100

# * / // %
print(2*2 + 4) # 8

