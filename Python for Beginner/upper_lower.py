

# upper method       to convert the string  to uppercase

s1 = "mahmoud"
s1 = s1.upper()
print(s1)          # MAHMOUD


# lower method       to convert the string  to lowercase

s2 = "MOHAMED"
s2 = s2.lower()
print(s2)          # mohamed


# capitalize method   to convert just first letter to uppercase
s3 = "mattar"
s3 = s3.capitalize()
print(s3)          # Mattar